class LogExceptions
{
    /**
     * Handle an incoming request.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \Closure  $next
     * @return mixed
     */
    public function handle($request, Closure $next)
    {
        try {
            return $next($request);
        } catch (Throwable $e) {
            Log::error('Exception caught by LogExceptions middleware', ['exception' => $e]);
            throw $e;
        }
    }
}
